
const test            = require('ava');
const sg              = require('..');

// -------------------------------------------------------------------------------------------------------------------
test('ava works', (t) => {
  t.pass();
});

// -------------------------------------------------------------------------------------------------------------------
test('pad pads', (t) => {
  const actual = sg.pad("foo", 7, ' ');
  t.is(actual, "    foo")
});

// -------------------------------------------------------------------------------------------------------------------
test('pad default space for padding', (t) => {
  const actual = sg.pad("foo", 7);
  t.is(actual, "    foo")
});

// -------------------------------------------------------------------------------------------------------------------
test('pad default zero for numbers', (t) => {
  const actual = sg.pad(42, 7);
  t.is(actual, "0000042")
});



// -------------------------------------------------------------------------------------------------------------------
// -------------------------------------------------------------------------------------------------------------------
test('splitN splits two', (t) => {
  const actual = sg.splitN('a/b/c/d', 2)

  t.deepEqual(actual, [['a', 'b'], 'c/d'])
});

// -------------------------------------------------------------------------------------------------------------------
test('splitN splits two, the right way', (t) => {
  const [parts, remainder] = sg.splitN('a/b/c/d', 2)

  t.deepEqual(parts, ['a', 'b'])
  t.is(remainder, 'c/d')
});

// -------------------------------------------------------------------------------------------------------------------
test('splitN handles too many splits', (t) => {
  const [parts, remainder] = sg.splitN('a/b', 3)

  t.deepEqual(parts, ['a', 'b'])
  t.is(remainder, '')
});

